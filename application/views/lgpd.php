					<div class="faq">
						<button class="accordion">
							<div>O que é a Lei Geral de Proteção de Dados – LGPD?</div>
						</button>
						<div class="panel">
							<p>A LGPD (Lei n.º 13.709/2018) dispõe sobre o tratamento de dados de pessoas físicas, ou seja, os cidadãos, seja em meio físico ou em meio digital.<br><br>
							A Lei se aplica a utilização de dados pessoais, por pessoas físicas ou jurídicas de direito público ou privado, e tem o objetivo de proteger os direitos fundamentais de liberdade e de privacidade e o livre desenvolvimento da personalidade da pessoa física.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quem é o titular dos dados pessoais?</div>
						</button>
						<div class="panel">
							<p>O titular dos dados pessoais é o cidadão, a quem pertencem os dados pessoais que são utilizados pela Prefeitura Municipal de Ponta Grossa.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>O que são os dados pessoais protegidos pela LGPD?</div>
						</button>
						<div class="panel">
							<p>Os dados pessoais são informações relativas a uma pessoa, identificada ou identificável (art. 5º, I da LGPD).<br>
							<b>Exemplo:</b> nome, RG, CPF, data e local de nascimento, telefone, endereço residencial, entre outros.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>O que são dados pessoais sensíveis previstos na LGPD?</div>
						</button>
						<div class="panel">
							<p>São considerados dados sensíveis aqueles que podem, de alguma forma, trazer algum tipo de discriminação para o cidadão, titular desses dados. Em outras palavras, são dados pessoais que poderão implicar riscos e vulnerabilidades potencialmente mais graves aos direitos e liberdades fundamentais dos titulares.<br><br>
								Os dados pessoais sensíveis são: dado pessoal sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico, quando vinculado a uma pessoa natural.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quem é o Controlador?</div>
						</button>
						<div class="panel">
							<p>O Controlador é a “pessoa natural ou jurídica, de direito público ou privado, a quem competem as decisões referentes ao tratamento de dados pessoais”, art. 5º, VI da Lei Geral de Proteção de Dados – LGPD (Lei n.º 13.709, de 2018).<br><br>
								Em outras palavras, o Controlador é o agente responsável por tomar as principais decisões referentes a utilização dos dados pessoais e por definir a finalidade do uso.<br><br>
								No caso em questão, o Controlador é a Prefeitura Municipal de Ponta Grossa.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quem é o Operador?</div>
						</button>
						<div class="panel">
							<p>O Operador é a “pessoa natural ou jurídica, de direito público ou privado, que realiza o tratamento de dados pessoais em nome do Controlador”, art. 5º, VII da Lei Geral de Proteção de Dados – LGPD (Lei n.º 13.709, de 2018).<br><br>
							Ou seja, o Operador é o agente responsável por realizar o uso de dados em nome do Controlador e conforme a finalidade por este delimitada.<br><br>
							No presente caso, Operadores são as pessoas físicas ou jurídicas, agentes públicos ou terceiros, que prestam algum tipo de serviço para a Prefeitura Municipal de Ponta Grossa e que, para a realização desse serviço, dados pessoais de cidadãos são fornecidos pela Prefeitura de acordo com a necessidade e finalidade.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quem é a Autoridade Nacional de Proteção de Dados (ANPD)?</div>
						</button>
						<div class="panel">
							<p>A ANPD é o “órgão da administração pública responsável por zelar, implementar e fiscalizar o cumprimento desta da LGPD em todo território nacional”, art. 5º, XIX da Lei Geral de Proteção de Dados – LGPD (Lei n.º 13.709, de 2018).<br><br>
								Além de monitorar e fiscalizar o cumprimento da LGPD, a ANPD é o órgão competente para emitir opiniões técnicas e recomendações para auxiliar as empresas e os órgãos públicos no processo de adequação à LGPD, bem como receber reclamações dos Titulares dos Dados sobre o mau uso ou vazamento de informações pessoais.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quem é o Encarregado de Proteção de Dados?</div>
						</button>
						<div class="panel">
							<p>O Encarregado de Proteção de Dados é a “pessoa indicada pelo Controlador e Operador para atuar como canal de comunicação entre o Controlador, os Titulares dos Dados e a Autoridade Nacional de Proteção de Dados (ANPD)”, art. 5º, VIII da Lei Geral de Proteção de Dados – LGPD (Lei n.º 13.709, de 2018).<br><br>
							De acordo com o art. 41 da LGPD, cada Controlador deverá indicar um Encarregado pelo tratamento de dados pessoais, que será responsável por receber solicitações de titulares e da Autoridade Nacional de Proteção de Dados, além de adotar as providências necessárias em relação à proteção de dados pessoais.<br><br>
							<p style='margin-left:5%'>
							Contato do Encarregado de Proteção de Dados Pessoais da Prefeitura Municipal de Ponta Grossa:<br>
							Dr. Samuel Airon Moreira<br>
							E-mail: lgpd@pontagrossa.pr.gov.br
							</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Qual a importância da LGPD para o Cidadão?</div>
						</button>
						<div class="panel">
							<p>A LGPD vem justamente para proteger a garantir os direitos fundamentais de liberdade e de privacidade do cidadão. A legislação é específica sobre privacidade e impõe, inclusive, multas pelo descumprimento de sua normativa.<br><br>
							Além de proteger e garantir os direitos fundamentais, a LGPD traz novos direitos ao cidadão com relação às suas informações, previstos no artigo 18 da Lei:<br>
							<b>1)</b> Confirmação da existência de tratamento;<br>
							<b>2)</b> Acesso aos dados;<br>
							<b>3)</b> Correção de dados incompletos, inexatos ou desatualizados;<br>
							<b>4)</b> Anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com o disposto na LGPD;<br>
							<b>5)</b> Portabilidade dos dados;<br>
							<b>6)</b> Eliminação dos dados pessoais (exceto quando o tratamento é legal, mesmo que sem o consentimento do titular);<br>
							<b>7)</b> Informação sobre compartilhamento de seus dados com entes públicos e privados, caso isso exista;<br>
							<b>8)</b> Informação sobre o não consentimento, ou seja, sobre a opção de não autorizar o tratamento e as consequências da negativa;<br>
							<b>9)</b> Revogação do consentimento.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>O que muda com a LGPD?</div>
						</button>
						<div class="panel">
							<p>A LGPD prevê um conjunto de ferramentas que, no âmbito público, aprofundam as obrigações de transparência ativa e passiva, reforçando a segurança dos dados e promovendo políticas mais transparentes sobre o uso, coleta, processamento e armazenamento dos dados pessoais dos cidadãos, além de garantir que todos os agentes vinculados à Prefeitura de Ponta Grossa cumpram com essa mesma obrigação.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Qual o papel do Agente público na LGPD?</div>
						</button>
						<div class="panel">
							<p>Todos os Agentes Públicos possuem o dever de zelar pela proteção dos dados pessoais dos cidadãos, garantir a privacidade e o respeito a todos os direitos fundamentais destes. Seja na coleta dos dados pessoais, como também nas atividades públicas em que o dado é utilizado e no seu armazenamento ou descarte pelo Poder Público.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quais as sanções administrativas previstas na LGPD?</div>
						</button>
						<div class="panel">
							<p>As sanções administrativas aplicáveis a partir de 1º agosto de 2021 são:<br><br>
							<ul>
							<li>Advertência;</li>
							<li>Multa simples (até 2% do faturamento até o limite de R$ 50 milhões);</li>
							<li>Multa diária;</li>
							<li>Possibilidade de publicização da infração;</li>
							<li>Bloqueio dos dados pessoais envolvidos;</li>
							<li>Eliminação dos dados pessoais envolvidos;</li>
							<li>Suspensão parcial, por até 06 (seis) meses do banco de dados envolvido; e</li>
							<li>Proibição parcial ou total do exercício de atividades relacionadas a tratamento de dados.</li>		
							</ul><br>
							As metodologias e para o cálculo do valor-base das multas serão definidas pela ANPD, através de regulamento próprio.</p>
						</div>
					</div>
					<div class="faq">
						<button class="accordion">
							<div>Quais destas sanções são aplicadas aos Órgãos Públicos?</div>
						</button>
						<div class="panel">
							<p>Os órgãos públicos estão sujeitos as seguintes sanções administrativas:<br><br>
							<ul>
							<li>Advertência, com indicação de prazo para adoção de medidas corretivas;</li>
							<li>Publicização da infração após devidamente apurada e confirmada a sua ocorrência;</li>
							<li>Bloqueio dos dados pessoais a que se refere a infração até a sua regularização;</li>
							<li>Eliminação dos dados pessoais a que se refere a infração;</li>
							<li>Suspensão parcial do funcionamento do banco de dados a que se refere a infração pelo período máximo de 6 (seis) meses, prorrogável por igual período, até a regularização da atividade de tratamento pelo controlador;</li>
							<li>Suspensão do exercício da atividade de tratamento dos dados pessoais a que se refere a infração pelo período máximo de 6 (seis) meses, prorrogável por igual período; </li>
							<li>Proibição parcial ou total do exercício de atividades relacionadas a tratamento de dados.</li>
							</ul><br>
								As sanções poderão ser aplicadas às entidades e aos órgãos públicos, sem prejuízo do disposto na Lei nº 8.112, de 11 de dezembro de 1990, na Lei nº 8.429, de 2 de junho de 1992, e na Lei nº 12.527, de 18 de novembro de 2011.</p>
						</div>
